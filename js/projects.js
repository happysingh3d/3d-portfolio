// Helper: detect if a media item is a video/3D iframe embed
function isIframeMedia(url) {
  if (typeof url !== "string") return false;
  const lower = url.toLowerCase();
  return (
    (lower.startsWith("http") && lower.includes("/player/")) ||
    lower.includes("adobe.io") ||
    lower.includes("vimeo.com/video") ||
    lower.includes("youtube.com/embed") ||
    lower.includes("youtu.be") ||
    lower.includes("/embed") ||
    lower.includes("sketchfab.com/models") ||
    lower.includes("block-city-mayhem") ||
    lower.includes("shardfall")
  );
}
